const RUNNER_FRAMES = [RUNNER_1, RUNNER_2, RUNNER_3, RUNNER_4];

// ─── Renderer ─────────────────────────────────────────────────────────────────
// Frame dimensions are read dynamically — each frame can have its own
// column/row count and will scale correctly to the canvas CSS display size.
function drawChar(canvas, frameIdx, sex) {
  const isFemale = (sex || '').toLowerCase() === 'female';

  const palette = {
  0: null,
  1: HAIR_HI,   2: HAIR_SH,
  3: SKIN_LT,   4: SKIN_SH,
  5: isFemale ? SHIRT_FEMALE : SHIRT_MALE,
  6: isFemale ? PATCH_FEMALE : PATCH_MALE,
  7: SHORTS_C,  8: SHORTS_SH,
  9: isFemale ? SOCK_FEMALE : SOCK_C,   // ← changed: sex-dynamic sock
  10: isFemale ? SOCK_FEMALE : SHOE_C, 
  11: EYE_C,                            // ← unchanged: same eye for both
  12: PANTS_C,
  13: PANTS_SH,
};

  const frame = RUNNER_FRAMES[frameIdx % 4];
  const ctx   = canvas.getContext('2d');
  const cols  = frame[0].length;
  const rows  = frame.length;
  const scaleX = canvas.width  / cols;
  const scaleY = canvas.height / rows;

  ctx.clearRect(0, 0, canvas.width, canvas.height);

  frame.forEach((row, y) =>
    row.forEach((c, x) => {
      const col = palette[c];
      if (!col) return;
      ctx.fillStyle = col;
      ctx.fillRect(
        Math.floor(x * scaleX), Math.floor(y * scaleY),
        Math.ceil(scaleX),      Math.ceil(scaleY)
      );
    })
  );
}